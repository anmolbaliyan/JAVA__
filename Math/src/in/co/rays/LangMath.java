package in.co.rays;
import java.lang.Math;

public class LangMath {
	public static void main(String[] args) {
		
		System.out.println("Mathematical Functions");
		System.out.println("Max 2,5 - " + Math.max(2, 5));
		System.out.println("Min 10,115 - " + Math.min(10, 115));
		System.out.println("Absolute of 4.88 - " + Math.abs(4.88));
		System.out.println("Exp 10 - " + Math.exp(10));
		System.out.println("Random Number - " + Math.random());
		System.out.println("Square Root - " + Math.sqrt(5));
	}

}
